def error(clf, X, y, ntrials=100, test_size=0.2) :
    """
    Computes the classifier error over a random split of the data,
    averaged over ntrials runs.

    Parameters
    --------------------
        clf         -- classifier
        X           -- numpy array of shape (n,d), features values
        y           -- numpy array of shape (n,), target classes
        ntrials     -- integer, number of trials

    Returns
    --------------------
        train_error -- float, training error
        test_error  -- float, test error
        f1_score    -- float, test "micro" averaged f1 score
    """

    ### ========== TODO : START ========== ###
    # compute cross-validation error over ntrials

    #initialize the error trackers to 0
    Train_error = 0
    Test_error = 0

    for i in range(ntrials):
        #first split the data into train and test split. 
        X_train, X_test, y_train, y_test = train_test_split(X, y, test_size = test_size, random_state=0)
        clf.fit(X_train,y_train)
        y_pred = clf.predict(X_train)

        #With the training y_pred, we can test its accuracy against the y_train
        train_error = 1 - metrics.accuracy_score(y_train, y_pred, normalize=True)
        Train_error += train_error

        #Now we move on to predicting the y using testing data
        #compare the y testing data with the predicted y from X_test
        y_pred = clf.predict(X_test)
        test_error = 1 - metrics.accuracy_score(y_test, y_pred, normalize=True)
        Test_error += test_error

    #grab the average errors and f1
    Train_error = float(Train_error)/((1-test_size) * len(y))
    Test_error = float(Test_error)/(test_size * len(y))
    f1 = f1_score(y_test, y_pred, average='micro')
  
    ### ========== TODO : END ========== ###
    return train_error, test_error, f1

